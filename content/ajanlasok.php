<h2>A stresszkutatás atyjának ajánlása</h2>

<p>
    Selye János (Hans Hugo Bruno Selye, 1907-1982) az osztrák-magyar származású kanadai orvos és vegyész,
    akit sokszor neveznek a "stresszkutatás atyjának", már 1926-ban, másodéves orvostanhallgatóként elkezdett dolgozni
    később híressé vált stressz-elméletén. Ez azt írja le, hogy az emberek miként képesek megbirkózni
    a sérülések és az intenzív élmények okozta terhekkel.
</p>

<p>
    Egész életén át folytatott kutatásai nyomán arra a következtetésre jutott,
    hogy a stressz minden betegség kifejlődésében szerepet játszik.
    Ha valaki nem képes megbirkózni a stresszekkel, az alkalmazkodási betegségekhez vezethet,
    mint amilyenek a fekélyek, a magas vérnyomás és számos egyéb probléma, amelyet a stressz okoz vagy súlyosbít
    (bővebben lásd: <a href="igazolt-hatasok" class="in-text" title="Ugrás az Igazolt hatások aloldalra">Igazolt hatások</a>).
</p>

<p>
    Az 1970-es évek közepén Dr. Selye Jánossal &ndash; aki három doktorátussal rendelkezett (MD, PhD és DSc),
    bevezette a modern orvostudományba a stressz fogalmát,
    43 alkalommal választották díszdoktorrá, 39 könyvet írt, és több mint 1700 szakcikket publikált a stresszről,
    valamint a vele összefüggő problémákról, ill. tíz alkalommal jelölték a Nobel-díjra &ndash; készült egy interjú,
    melyben a Transzcendentális Meditációról nyilatkozik.
    Ez az interjú megtalálható Robert Oates <i>"Celebrating the Dawn"</i> című könyvében, ebből következik most egy részlet:
</p>

<blockquote class="short">
    Dr. Selye János:<br>
    <p>&ndash; Nagy szerencsém volt, hogy egyik nemzetközi szimpóziumán majdnem egy teljes napot tölthettem Maharishi Mahesh Yogival. Azóta úgy érzem, hogy rendkívül gyümölcsöző lenne részletes kutatásokat végezni azzal kapcsolatban, hogy a Transzcendentális Meditáció milyen hatással van a stresszre az emberi szervezetben.</p>
    <p>A kutatások már rávilágítottak arra, hogy a Transzcendentális Meditáció fiziológiai hatásai pontosan ellenkezői azoknak, amelyeket a modern orvostudomány leír, amikor a testnek a stresszek kihívásaival való megküzdését jellemzi.</p>

    <p>
        <b>&ndash; Amikor azt mondja, hogy a Transzcendentális Meditáció hatásai pontosan fordítottjai azoknak, amelyek a stressz nyomán fellépnek, akkor ez alatt azt érti, hogy ezek a hatások azokkal ellenkeznek, amikor a testet igénybevétel éri?<br></b>
        &ndash; Igen, a kutatások ezt mutatják az anyagcsere, a légzés, a bőrellenállás, a vérlaktát-szintje, az agyhullámok, valamint a szív- és érrendszer vonatkozásában. Ugyanígy, a Transzcendentális Meditációnak a testi zavarokra gyakorolt gyógyító hatásai a stressz, illetve a stresszel való megküzdés (adaptáció) nyomán létrejövő problémák esetén a legnyilvánvalóbbak. Különösképpen az olyan megbetegedéseknél, amelyek a mindennapi élet stresszeire adott helytelen válaszok nyomán alakulnak ki, mint amilyenek a mentális, a szív- és érrendszeri, a gasztrointesztinális, valamint a hiperszenzitív zavarok.<br>
    </p>

    <p>
        <b>&ndash; Maharishi kijelentette, hogy ha valaki folyamatosan gyakorolja a meditációt, akkor a szervezete fokozatosan feloldja a stresszeket, és végső soron lehetővé válik számára, hogy egy teljesen stresszek nélküli életet éljen. Lehetségesnek látja ezt az emberi idegrendszer számára?<br></b>
        &ndash; Ez a kijelentés a stressz fogalmának egy eltérő meghatározásán alapszik. Úgy gondolom, hogy bár ez az interjú a laikusok számára szól, mégis tartanunk kell magunkat a tudományos pontossághoz. Ha a stressz orvosi definícióját használjuk, akkor sem a Transzcendentális Meditáció, sem más módszer vagy orvosság célja nem lehet az, hogy az összes stresszt felszámolja.<br>
    </p>

    <p>
        <b>&ndash; Miért nem?<br></b>
        &ndash; Mert egy adott stressz szint nélkül most még beszélgetni sem volnánk képesek!<br>
    </p>

    <p>
        <b>&ndash; Stresszre van szükség ahhoz, hogy beszélgessünk?<br></b>
        &ndash; A stressz definíciója egy meghatározott kihívásra adott reakció. Még alvás vagy a műtétek során történő altatás közben is ér minket valamennyi stressz, mivel a testünk bizonyos részei használatban vannak. Például a szívünk ver, folyamatosan lélegzünk.<br>
    </p>

    <p>
        <b>&ndash; Egyszerűen fogalmazva: a Maharishi által adott definíció a stressz negatív oldalát emeli ki, míg az orvosi meghatározás szerint a stressz jelölhet negatív vagy pozitív dolgot is.<br></b>
        &ndash; Igen. Amikor ezt a fogalmat megalkottam, nem gondoltam erre a különbségre, egyszerűen elneveztem stressznek. Ám később gyakran összekavarodtak a fogalmak. A köznyelv szinonim értelemben használja a stressz és a distressz fogalmát, noha ezek egyáltalán nem jelentik ugyanazt. A fájdalom, a bánat, az idegesség vagy a szenvedés stressze, a rossz stressz az, amit distressznek nevezünk. Ám ott van az alkotás stressze, annak a stressze, hogy önmagunkat gyógyító módon kezeljük a dolgokat. Ezt nem akarhatjuk felszámolni! Tehát létezik jó stressz (szaknyelven “eustressz”), valamint rossz stressz (distressz), ám a bármilyen igénybevételre adott reakciót általánosan stressznek nevezzük. Stressz mindig van, az egyetlen dolog, amire ügyelnünk kell, hogy önmagunk és mások számára hasznossá tegyük.<br>
    </p>

    <p>
        <b>&ndash; Akkor e fogalmakat használva miként határozná meg a Transzcendentális Meditáció hasznát?<br></b>
        &ndash; Olyan módszerként utalnék rá, amely ellazítja az ember központi idegrendszerét, és az így könnyebben él együtt a stresszekkel, nem szenved annyit a stressztől, és nem veszíti el annak összes hasznos, ösztönző hatását. A Transzcendentális Meditáció mindenfajta tevékenységre felkészíti az idegrendszert. Végső soron ugyanis az idegrendszernek van a legfőbb szerepe abban, hogy kellemes vagy kellemetlen stresszt élünk át, hogy elégedettek vagyunk vagy elégedetlenek. És úgy gondolom, ha képesek vagyunk oly módon hatást gyakorolni az idegrendszerre a Transzcendentális Meditáción keresztül, hogy az valóban lenyugodjon, valóban a legjobbat nyújtsa abban, hogy nem ad specifikus választ semmilyen igénybevételre, akkor az egy ideális megoldás.<br>
    </p>

