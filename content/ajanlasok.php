A huszadik században vezető szaktekintélynek tartották a stressz emberi egészségre gyakorolt hatása terén.
Selye János gyakorolta a Transzcendentális Meditáció technikát.
1955-ben Dr. Veér András a Népjóléti Minisztérium Mentálhygiéne miniszteri biztosa,
aki az Országos Pszichiátriai és Neurológiai Intézet Főigazgató Főorvosa is,
írt egy ajánló levelet, melyben a Transzcendentális Meditációt ajánlja.
</p>

<p>
1970-es évek közepén Dr. Selye Jánossal (aki három doktorátussal rendelkezett (MD, PhD és DSc),
bevezette a modern orvostudományba a stressz fogalmát,
43 alkalommal választották díszdoktorrá, 39 könyvet írt, és több mint 1700 szakcikket publikált a stresszről,
valamint a vele összefüggő problémákról, ill. tíz alkalommal jelölték a Nobel-díjra) készült egy interjú,
melyben a Transzcendentális Meditációról nyilatkozik.
Ez az interjú megtalálható Robert Oates “Celebrating the Dawn” című könyvében.
